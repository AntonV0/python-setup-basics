"""Core Data Types"""

# Integer (int)
x = 5
print(type(x))

# Floating point numbers (float)
y = 3.14
print(type(y))

# String (str)
z = "Hello, World!"
print(type(z))

# Boolean (bool)
a = True
print(type(a))

# Reassign the variable data type and point to it
y = 'new data'
print(type(y))
y = 8.9
print(type(y))
